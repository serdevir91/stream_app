import 'package:flutter_riverpod/flutter_riverpod.dart';

import '../settings/app_settings_provider.dart';

class AppText {
  final String languageCode;

  const AppText(this.languageCode);

  String t(String key) {
    final selected = _translations[languageCode] ?? _translations['en']!;
    final fallback = _translations['en']!;
    return selected[key] ?? fallback[key] ?? key;
  }
}

final appTextProvider = Provider<AppText>((ref) {
  final settings = ref.watch(appSettingsProvider);
  return AppText(settings.appLanguage);
});

